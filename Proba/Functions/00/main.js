
function compa(num1, num2) {
    if (num1 === num2) {
        console.log('Son iguales')

    } else if (num1 > num2) {
        console.log(num1 + ', es mayor que ' + num2)

    } else console.log(num2 + ', es mayor que ' + num1)
}

// compa(4, 8)

function bool(num1, num2) {
    if (num1 === num2) console.log('True')
    else console.log('False')
}

// bool(8, 4)

